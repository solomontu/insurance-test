import 'package:flutter/material.dart';

class ThemeModel {
  // This is the theme class where all the theme colors are defined.
  ThemeModel({
    required this.white,
    required this.black,
    required this.green,
    required this.textBlack,
    required this.subText,
    required this.purple,
    required this.purpleLight,
    required this.offWhite,
    required this.textGray,
    required this.subText2,
    required this.subText3,
    required this.green2,
    required this.fadedBlack,
    required this.fadedBlack2,
    required this.borderBlack,
  });

  final Color white;
  final Color black;
  final Color textBlack;
  final Color green;
  final Color green2;
  final Color subText;
  final Color purple;
  final Color purpleLight;
  final Color offWhite;
  final Color textGray;
  final Color subText2;
  final Color subText3;
  final Color fadedBlack;
  final Color fadedBlack2;
  final Color borderBlack;
}
